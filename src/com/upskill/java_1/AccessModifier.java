package com.upskill.java_1;

public class AccessModifier {
	
	
	// Public- Any class can access
	// Private- Only same class can access
	// Protected- Classes in the same package and subclass
	// Default- Classes in same package can access
	
	
	public String name = "Tuhin"; 		//Public- Any class can access		
	private int age = 28;				//Private- Only same class can access
	protected int snn = 12345678;		//Protected- Classes in the same package and subclass
	
		String address = "Heritage Tower"; // Default- Classes in same package can access

}


